# ชื่อตัวละคร/สถานที่ whitelist
CUSTOM_WORDS = ["โดคุริสึ", "ชิตะ"]


# ============================================================
# helpers
# ============================================================

def separator(title: str):
    print(f"\n{'='*60}")
    print(f"  {title}")
    print('='*60)


# ============================================================
# TEST 1: ไม่มี custom_words (ดู false positive)
# ============================================================

separator("TEST 1 — ไม่มี custom_words")
t0 = time.time()
result_no_custom = check_novel_text(TEXT, custom_words=None)
elapsed = time.time() - t0

print(f"เวลา        : {elapsed:.2f}s")
print(f"จำนวนคำ     : {result_no_custom.total_words}")
print(f"พบคำผิด     : {result_no_custom.error_count} คำ")
print()
for err in result_no_custom.errors:
    print(f"  ❌ {err.word!r:20s}  offset={err.offset:<5d}  suggestions={err.suggestions[:3]}")
    print(f"     context: ...{err.sentence[:80]}...")


# ============================================================
# TEST 2: มี custom_words (ดูว่าชื่อตัวละครหายไปไหม)
# ============================================================

separator("TEST 2 — มี custom_words")
t0 = time.time()
result_with_custom = check_novel_text(TEXT, custom_words=CUSTOM_WORDS)
elapsed = time.time() - t0

print(f"เวลา        : {elapsed:.2f}s")
print(f"จำนวนคำ     : {result_with_custom.total_words}")
print(f"พบคำผิด     : {result_with_custom.error_count} คำ")
print(f"skip custom : {result_with_custom.custom_words_used}")
print()
for err in result_with_custom.errors:
    print(f"  ❌ {err.word!r:20s}  offset={err.offset:<5d}  suggestions={err.suggestions[:3]}")
    print(f"     context: ...{err.sentence[:80]}...")


# ============================================================
# TEST 3: cache warmup — รันซ้ำดูว่าเร็วขึ้นไหม
# ============================================================

separator("TEST 3 — cache warmup (รันซ้ำ)")
t0 = time.time()
result_cached = check_novel_text(TEXT, custom_words=CUSTOM_WORDS)
elapsed = time.time() - t0
print(f"เวลา (ครั้งที่ 2): {elapsed:.2f}s  ← ควรเร็วกว่า TEST 2")


# ============================================================
# TEST 4: คำผิดจงใจ
# ============================================================

separator("TEST 4 — คำผิดจงใจ")
TYPO_TEXT = "เขากนิข้าวอยู่ที่บ้น แล้วก็ดื่มน้ำเย็นๆ"
result_typo = check_novel_text(TYPO_TEXT)
print(f"input: {TYPO_TEXT!r}")
print(f"พบคำผิด: {result_typo.error_count} คำ")
for err in result_typo.errors:
    print(f"  ❌ {err.word!r} → {err.suggestions[:3]}")


# ============================================================
# สรุป
# ============================================================

separator("สรุป")
false_positives = set(e.word for e in result_no_custom.errors) - set(e.word for e in result_with_custom.errors)
print(f"false positive ที่หายไปเมื่อใส่ custom_words : {false_positives}")
print(f"errors ที่เหลืออยู่หลัง custom_words         : {[e.word for e in result_with_custom.errors]}")
